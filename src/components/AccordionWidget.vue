<template>
  <div class="accordions">
    <template v-for="(accordion, index) in updatedAccordions" :key="index">
      <h3 class="accordionHeading">
        <button
          :aria-expanded="accordion.expanded"
          class="Accordion-trigger"
          :aria-controls="index + '-content'"
          :id="index + '-id'"
          @click="updateAccordion(accordion, accordion.multiple)"
        >
          {{ accordion.heading }}
          <span class="expandIcons" aria-hidden="true">
            <span v-if="accordion.expanded">-</span>
            <span v-else>+</span>
          </span>
        </button>
      </h3>
      <div
        :id="index + '-content'"
        role="region"
        :aria-labelledby="index + '-id'"
        class="accordion-panel"
        v-if="accordion.expanded"
      >
        {{ accordion.content }}
      </div>
    </template>
  </div>
</template>

<script>
export default {
  props:['accordions'],
  computed: {
    updatedAccordions() {
      return this.accordions;
    },
  },
  methods: {
    updateAccordion(accordion, multiple) {
      if (multiple) {
        accordion.expanded = !accordion.expanded;
      } else {
        for (let i = 0; i < this.accordions.length; i++) {
          if (accordion.id == this.accordions[i].id) {
            this.updatedAccordions[i].expanded = !this.updatedAccordions[i].expanded;
          } else {
            this.updatedAccordions[i].expanded = false;
          }
        }
      }
    },
  },
};
</script>

